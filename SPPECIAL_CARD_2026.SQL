DECLARE
CURSOR C
IS
SELECT BRAND_CODE,FF_TYPE,PROD_GRP FROM
(
SELECT DISTINCT A.BRAND_CODE ,FF_TYPE, B.PROD_GRP
FROM
SPEC_GIFT_CARD_CAMP_MST_ALLOC A, (SELECT BRAND_CODE, PROD_GRP FROM PRODUCT) B
WHERE A.BRAND_CODE = B.BRAND_CODE
)
WHERE PROD_GRP <>FF_TYPE;

BEGIN
FOR REC IN C LOOP

UPDATE SPEC_GIFT_CARD_CAMP_MST_ALLOC
        SET FF_TYPE          = REC.PROD_GRP
WHERE   BRAND_CODE             = REC.BRAND_CODE;

END LOOP;
END;
